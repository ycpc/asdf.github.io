                    <h4 class="service-heading" style = "margin-bottom:10px;">YCPC Online Coding Courses</h4>
                    <p class="text-muted" style = "font-size:12px">YCPC courses provide quick and compact access to advanced programming concepts. Designed for younger audiences, the courses offer jargon-free explanations and excellent reference materials. Currently YCPC offers Beginners Java, Beginners Python, Web Fundamentals, and Advanced Web, with our MCC tutor course, Advanced Java and Advanced Python to be released in the summer of 2016.</p>
