                    <h4 class="service-heading" style = "margin-bottom:10px;">Minecraft Coding Camp</h4>
                    <p class="text-muted" style = "font-size:12px">A 2 day coding workshop for middle and high schoolers, YCPC’s Minecraft Coding Camp uses the ComputerCraft mod to cut out the intensive memorization characteristic to traditional pedagogy. Students will learn Lua by programming in-game robots to automate various in-game tasks ranging from ore mining to zombie fighting.</p>
