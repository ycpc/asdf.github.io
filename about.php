                            <p class="text-muted" style = "font-size:12px">Through the Bridge Program, YCPC hosts and helps other schools and clubs set up field trips to tech companies located in the Bay Area. The Bridge Program is designed to inspire students about STEM by organizing and facilitating trips to tech . Participants get to see that working in computer science is not just cubicle-work, but also collaboration in dynamic, team-based facilities which are famous for their great atmospheres.</p>
