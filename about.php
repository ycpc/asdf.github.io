                        <p class="text-muted" style = "font-size:12px">Youth Competitive Programming Circle’s Library Coding Initiative provides libraries with the resources to bring computer science education to disadvantaged youth. Through the program, YCPC assists librarians and other interested community leaders in starting clubs, holding classes, and providing the requisite software and coding resources to allow students to jumpstart their computer science careers.</p>
